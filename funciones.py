# abs funcion que devuelde el valor absoluto de un numero
print(abs(-4/3))

# round funcion que redondea un numero decimal
print(round(4/3))

# min funcion que regresa el valor minimo de una cadeja de numeros
print(min(30,15,27,58,63))

# max funcion que regresa el valor maximo de una cadeja de numeros
print(max(30,15,27,58,63))

# sorted ordena una lista de forma acendente, las cadenas de texto secun el abc
numeros = [7,3,9,2,5,1,4,8,6]
letras = ["g","d","a","f","c","e","b"]

print(sorted(numeros))
print(sorted(letras))

# sum funcion que suma los numeros de una lista
print(sum(numeros))

# lower y upper funcion que devuelve una cadena en minuscula o mayuscula
Color="   MorAdO  "
print(Color.lower())
print(Color.upper())

# strip funcion que quita los esptacion de una cadena
print(Color.strip())

# replace funcion que remplaza una palabra en un string
hola = "hello mundo"
print(hola.replace("mundo","world"))

# split funcion que separa una cadena y devuelve una lista

codigo="codigo-facilito"

print(codigo.split("-"))
